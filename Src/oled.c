HAL_StatusTypeDef OLED_Cmd(uint8_t data)
{
	uint8_t BufData[2];
	BufData[0]=0x80;
	BufData[1]=data;
	return ( HAL_I2C_Master_Transmit(&hi2c1,OLED_ADDRESS, BufData, 2,0xffffffff ) );
			//(I2C_Master_BufferWrite(I2C1, BufData,2,DMA, 0x78));
}

HAL_StatusTypeDef OLED_Data(uint8_t data)
{
	uint8_t BufData[3];
	BufData[0]=0x40;
	BufData[1]=data;
	return(HAL_I2C_Master_Transmit(&hi2c1,OLED_ADDRESS, BufData, 2,100000 ));
}

void SetPosChar(uint8_t x, uint8_t y)
{
	SetColumn(x*6);
	SetPage(y);
}

void SetPos(uint8_t x, uint8_t y)
{
	SetColumn(x);
	SetPage(y);
}

void CharMedium(uint8_t cyfra, uint8_t x,uint8_t y )
{
	int i;
	SetPos(x*10,y*2);
	for(i=cyfra*20;i<cyfra*20+10;i++)
		OLED_Data(dig_mid[i]);

	SetPos(x*10,(y*2)+1);
	for(i=cyfra*20+10;i<cyfra*20+20;i++)
		OLED_Data(dig_mid[i]);

}

void CharBig(uint8_t cyfra, uint8_t x,uint8_t y )
{
	int i;
	SetPos(x*15,y*3);
	for(i=cyfra*45;i<cyfra*45+15;i++)
		OLED_Data(dig_big[i]);

	SetPos(x*15,(y*3)+1);
	for(i=cyfra*45+15;i<cyfra*45+30;i++)
		OLED_Data(dig_big[i]);

	SetPos(x*15,(y*3)+2);
	for(i=cyfra*45+30;i<cyfra*45+45;i++)
		OLED_Data(dig_big[i]);

}

void WriteChar(char code)
{
int code_point;

	code_point=((int)code*6);

	OLED_Data(rom_gen[code_point++]);
	OLED_Data(rom_gen[code_point++]);
	OLED_Data(rom_gen[code_point++]);
	OLED_Data(rom_gen[code_point++]);
	OLED_Data(rom_gen[code_point++]);
	OLED_Data(rom_gen[code_point]);

}

void OledTxt(const char *napis,char x,char y)
{
	SetPosChar(x,y);//pozycja tekstu na wyswietlaczu
    while(*napis!=0)
	{WriteChar(*napis);
    ++napis;}
}

uint8_t SetColumn(uint8_t column)
{
	if(column>128)
		return(1);
	++column;
	++column;
	OLED_Cmd(COLADDLOW|(column&0x0f));
	OLED_Cmd(COLADDHIGH|(column>>4));
	return(0);
}

uint8_t SetLine(uint8_t line)
{
	if(line>32)
	return(1);
	OLED_Cmd(LINEADD|line);
	return(0);
}

uint8_t SetPage(uint8_t page)
{
	if(page>7)
	return(1);
	OLED_Cmd(PAGEADD|page);
	return(0);
}

void OledBmp(void)
{
	uint16_t i,j,k;
	SetColumn(0);
	SetLine(0);
	SetPage(0);
	k=0;
	for(j=0;j<8;j++)
	{
		SetPage(j);
		SetColumn(0);
		for (i=0; i<128; i++)
		{
			OLED_Data(bmp[k++]);
		}
	}
}

void OledCls(void)
{
	uint16_t i,j;
	SetColumn(0);
	SetLine(0);

	for(j=0;j<8;j++)
	{
		SetPage(j);
		SetColumn(0);
		for (i=0; i<128; i++)
		{
			OLED_Data(0x00);
		}
	}
}


